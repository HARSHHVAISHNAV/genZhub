function toggleDropdown() {
  var dropdown = document.getElementById("myDropdown");
  if (dropdown.style.display = "none") {
      dropdown.style.display = "block";
  } else {
      dropdown.style.display = "block";
  }
}
function toggleDropdownLeave() {
  var dropdown = document.getElementById("myDropdown");
      dropdown.style.display = "none";
} 